# Description: A Python program that calculates the area and perimeter of a rectangle.

length = 10
width = 4

area = length * width
perimeter = 2 * (length + width)

print("For a rectangle with length {}cm and width {}cm:".format(length, width))
print("The area is {} square centimeters.".format(area))
print("The perimeter is {} centimeters.".format(perimeter))
